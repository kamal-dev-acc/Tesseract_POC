package poc.icici.tess;

public class BusinessRuleObject {

	private int ruleId;
	private String setOfRules;
	private String createdDate;
	private int masterFileId;
	
	/**
	 * @return the ruleId
	 */
	public int getRuleId() {
		return ruleId;
	}
	/**
	 * @param ruleId the ruleId to set
	 */
	public void setRuleId(int ruleId) {
		this.ruleId = ruleId;
	}
	/**
	 * @return the setOfRules
	 */
	public String getSetOfRules() {
		return setOfRules;
	}
	/**
	 * @param setOfRules the setOfRules to set
	 */
	public void setSetOfRules(String setOfRules) {
		this.setOfRules = setOfRules;
	}
	/**
	 * @return the createdDate
	 */
	public String getCreatedDate() {
		return createdDate;
	}
	/**
	 * @param createdDate the createdDate to set
	 */
	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}
	/**
	 * @return the masterFileId
	 */
	public int getMasterFileId() {
		return masterFileId;
	}
	/**
	 * @param masterFileId the masterFileId to set
	 */
	public void setMasterFileId(int masterFileId) {
		this.masterFileId = masterFileId;
	}

	
	
}
