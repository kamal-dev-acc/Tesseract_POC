package poc.icici.tess;

public class MasterTableObject {
	
	private int masterFileId;
	private String masterFileName;
	private String masterFileLocation;
	private String createdDate;
	
	
	/**
	 * @return the masterFileId
	 */
	public int getMasterFileId() {
		return masterFileId;
	}
	/**
	 * @param masterFileId the masterFileId to set
	 */
	public void setMasterFileId(int masterFileId) {
		this.masterFileId = masterFileId;
	}
	/**
	 * @return the masterFileLocation
	 */
	public String getMasterFileLocation() {
		return masterFileLocation;
	}
	/**
	 * @param masterFileLocation the masterFileLocation to set
	 */
	public void setMasterFileLocation(String masterFileLocation) {
		this.masterFileLocation = masterFileLocation;
	}
	/**
	 * @return the masterFileName
	 */
	public String getMasterFileName() {
		return masterFileName;
	}
	/**
	 * @param masterFileName the masterFileName to set
	 */
	public void setMasterFileName(String masterFileName) {
		this.masterFileName = masterFileName;
	}
	/**
	 * @return the createdDate
	 */
	public String getCreatedDate() {
		return createdDate;
	}
	/**
	 * @param createdDate the createdDate to set
	 */
	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}
	
	
	

}
